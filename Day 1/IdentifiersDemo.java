
public class IdentifiersDemo {


	public static void main(String[] args) {
		
		//Valid identifiers
		
		int no=12;
		System.out.println(no);
		
		int no2=133;
		System.out.println(no2);
		
		float no3=56.88f;
		System.out.println(no3);
		
		double _no4=45367.99d;
		System.out.println(_no4);
		
		int $no2=133;
		System.out.println($no2);
		
		//invalid identifiers
		//int no 7=45;
		//int #no8=23;
		//int for=89;

	}

}

